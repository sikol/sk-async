#include <cstdio>
#include <iostream>
#include <ranges>

#include <fmt/core.h>

#include "sk/cio.hxx"

auto handle_client(sk::net::tcpchannel client) -> sk::task<void> {
    for (;;) {
        sk::fixed_buffer<std::byte, 1024> buf;

        auto ret = co_await sk::async_read_some(client, buf);
        if (!ret) {
            fmt::print(stderr, "read err: {}\n", ret.error().message());
            co_await client.async_close();
            co_return;
        }

        for (auto &&range : buf.readable_ranges()) {
            std::cout.write(
                reinterpret_cast<char const *>(std::ranges::data(range)),
                std::ranges::size(range));
        }

        auto wret = co_await sk::async_write_all(client, buf);
        if (wret.second) {
            co_await client.async_close();
            co_return;
        }
    }

    fmt::print(stderr, "handle_client() : return\n");
}

auto run(std::string const &addr, std::int16_t port) -> sk::task<void> {
    auto ep = sk::net::make_tcp_endpoint(addr, port);
    if (!ep) {
        fmt::print(stderr, "{}:{}: {}\n", addr, port,
                   ep.error().message());
        co_return;
    }

    auto server = sk::net::tcpserverchannel::listen(*ep);

    for (;;) {
        auto client = co_await server->async_accept();
        if (!client) {
            fmt::print(stderr, "async_accept(): {}\n",
                       client.error().message());
            co_return;
        }

        sk::co_detach(handle_client(std::move(*client)));
    }
}


auto main(int argc, char **argv) -> int try {
    using namespace std::chrono_literals;

    if (argc != 3) {
        fmt::print(stderr, "usage: {} <address> <port>", argv[0]);
        return 1;
    }

    sk::reactor_handle reactor;
    wait(run(argv[1], std::atoi(argv[2])));
    return 0;
} catch (std::exception const &e) {
    std::cerr << "unexpected exception: " << e.what() << '\n';
    return 1;
}
